# Process each language file
for lang_code, translations in warehouses_translations.items():
    file_path = f'frontend/src/locales/{lang_code}.json'
    
    try:
        # Read existing translations
        with open(file_path, 'r', encoding='utf-8') as f:
            data = json.load(f)
        
        # Add warehouses section
        data['warehouses'] = translations
        
        # Write back to file
        with open(file_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        
        print(f'✓ Added warehouses translations to {lang_code}.json')
    
    except Exception as e:
        print(f'✗ Error processing {lang_code}.json: {e}')

print('\nWarehouses translations added successfully!')
