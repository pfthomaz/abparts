for lang_code, orders_translations in translations.items():
    filename = f'frontend/src/locales/{lang_code}.json'
    
    try:
        with open(filename, 'r', encoding='utf-8') as f:
            data = json.load(f)
        
        # Add orders section
        data['orders'] = orders_translations
        
        with open(filename, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        
        print(f"✅ Updated {lang_code}.json with Orders translations")
    except Exception as e:
        print(f"❌ Error updating {lang_code}.json: {e}")

